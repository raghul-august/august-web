import { useCallback, useRef, type RefObject } from "react";
import { trackToolEvent } from "@/app/utils/analytics";

interface UseDownloadResultOptions {
  toolId: string;
  filename: string;
  heading: string;
  subtitle: string;
  toolName: string;
  target?: string;
  maxPageHeight?: number;
}

interface UseDownloadResultReturn {
  resultRef: RefObject<HTMLDivElement | null>;
  handleDownload: () => Promise<void>;
}

const CAPTURE_PADDING = 24;
const CAPTURE_PADDING_X = 86;
const DEFAULT_MAX_PAGE_HEIGHT = 1250;
const PAGE_BREAK_GUTTER = 40;
const CAPTURE_PAGE_WIDTH = 720;

function sanitize(name: string): string {
  return name.replace(/[^a-z0-9]+/gi, "-").replace(/^-+|-+$/g, "").toLowerCase() || "report";
}

function buildHeaderElement(
  title: string,
  subtitle: string,
  toolName: string,
  url: string | null,
): {
  element: HTMLDivElement;
  toolNameLink: HTMLAnchorElement | null;
  logoImage: HTMLImageElement;
} {
  const el = document.createElement("div");
  el.style.background = "#206E55";
  el.style.color = "#ffffff";
  el.style.padding = "22px 22px";
  el.style.borderRadius = "0";
  el.style.display = "flex";
  el.style.flexDirection = "column";
  el.style.gap = "28px";
  el.style.textAlign = "left";
  el.style.marginTop = `-${CAPTURE_PADDING}px`;
  el.style.marginLeft = `-${CAPTURE_PADDING_X}px`;
  el.style.marginRight = `-${CAPTURE_PADDING_X}px`;
  el.style.marginBottom = "20px";

  const titleRow = document.createElement("div");
  titleRow.style.display = "flex";
  titleRow.style.alignItems = "center";
  titleRow.style.gap = "14px";

  const logoImage = document.createElement("img");
  logoImage.src = "/icons/icon-192x192.png";
  logoImage.alt = "August";
  logoImage.style.width = "44px";
  logoImage.style.height = "44px";
  logoImage.style.borderRadius = "8px";
  logoImage.style.display = "block";
  logoImage.style.flexShrink = "0";
  titleRow.appendChild(logoImage);

  const titleBlock = document.createElement("div");
  titleBlock.style.display = "flex";
  titleBlock.style.flexDirection = "column";
  titleBlock.style.gap = "2px";

  const titleEl = document.createElement("div");
  titleEl.textContent = title;
  titleEl.style.fontSize = "22px";
  titleEl.style.fontWeight = "500";
  titleEl.style.letterSpacing = "-0.01em";
  titleEl.style.lineHeight = "1.2";
  titleBlock.appendChild(titleEl);

  const subtitleEl = document.createElement("div");
  subtitleEl.textContent = subtitle;
  subtitleEl.style.fontSize = "14px";
  subtitleEl.style.fontWeight = "400";
  subtitleEl.style.color = "rgba(255,255,255,0.78)";
  subtitleEl.style.letterSpacing = "-0.01em";
  subtitleEl.style.lineHeight = "1.2";
  titleBlock.appendChild(subtitleEl);  
  titleRow.appendChild(titleBlock);
  el.appendChild(titleRow);

  const todayLabel = new Date().toLocaleDateString(undefined, {
    year: "numeric",
    month: "short",
    day: "numeric",
  });

  const generatedLine = document.createElement("div");
  generatedLine.style.fontSize = "13px";
  generatedLine.style.color = "rgba(255,255,255,0.85)";
  generatedLine.style.letterSpacing = "-0.01em";
  generatedLine.style.lineHeight = "1.2";
  generatedLine.appendChild(document.createTextNode("Generated by "));
  generatedLine.style.alignSelf = "flex-end";
  generatedLine.style.textAlign = "right";
  
  let toolNameLink: HTMLAnchorElement | null = null;
  if (url) {
    toolNameLink = document.createElement("a");
    toolNameLink.href = url;
    toolNameLink.textContent = toolName;
    toolNameLink.style.color = "#ffffff";
    toolNameLink.style.fontWeight = "600";
    toolNameLink.style.textDecoration = "underline";
    generatedLine.appendChild(toolNameLink);
  } else {
    generatedLine.appendChild(document.createTextNode(toolName));
  }

  generatedLine.appendChild(document.createTextNode(` on ${todayLabel}`));
  el.appendChild(generatedLine);

  return { element: el, toolNameLink, logoImage };
}

