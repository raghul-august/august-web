export function useDownloadResult({
  toolId,
  filename,
  heading,
  subtitle,
  toolName,
  target = "download_report",
  maxPageHeight = DEFAULT_MAX_PAGE_HEIGHT,
}: UseDownloadResultOptions): UseDownloadResultReturn {
  const resultRef = useRef<HTMLDivElement>(null);

  const handleDownload = useCallback(async () => {
    if (!resultRef.current) return;
    const node = resultRef.current;
    const pageUrl = typeof window !== "undefined" ? window.location.href : null;
    const { element: headerEl, toolNameLink, logoImage } = buildHeaderElement(heading, subtitle, toolName, pageUrl);
    const { element: footerEl, qrImage } = buildFooterElement();

    const pageBg =
      getComputedStyle(document.body).backgroundColor || "#FAF9F5";
    const overlay = document.createElement("div");
    overlay.setAttribute("aria-hidden", "true");
    overlay.style.position = "fixed";
    overlay.style.top = "0";
    overlay.style.left = "0";
    overlay.style.right = "0";
    overlay.style.bottom = "0";
    overlay.style.background = pageBg;
    overlay.style.zIndex = "2147483646";
    overlay.style.pointerEvents = "none";

    const skipEls = Array.from(
      node.querySelectorAll<HTMLElement>('[data-skip-screenshot="true"]'),
    );
    const prevSkipDisplays = skipEls.map((el) => el.style.display);
    skipEls.forEach((el) => {
      el.style.display = "none";
    });
    document.body.appendChild(overlay);
    node.insertBefore(headerEl, node.firstChild);
    node.appendChild(footerEl);
    const prevPadding = node.style.padding;
    const prevBorderRadius = node.style.borderRadius;
    const prevOverflow = node.style.overflow;
    const prevMarginLeft = node.style.marginLeft;
    const prevMarginRight = node.style.marginRight;
    const prevPosition = node.style.position;
    const prevTop = node.style.top;
    const prevLeft = node.style.left;
    const prevWidth = node.style.width;
    const prevMaxWidth = node.style.maxWidth;
    const prevBoxSizing = node.style.boxSizing;
    node.style.padding = `${CAPTURE_PADDING}px ${CAPTURE_PADDING_X}px`;
    node.style.borderRadius = "0";
    node.style.overflow = "visible";
    node.style.marginLeft = "0";
    node.style.marginRight = "0";
    node.style.position = "fixed";
    node.style.top = "0";
    node.style.left = "0";
    node.style.width = `${CAPTURE_PAGE_WIDTH}px`;
    node.style.maxWidth = "none";
    node.style.boxSizing = "border-box";
    const spacerEls: HTMLDivElement[] = [];
    try {
      await Promise.all(
        [qrImage, logoImage].map(
          (img) =>
            new Promise<void>((resolve) => {
              if (img.complete) return resolve();
              img.onload = () => resolve();
              img.onerror = () => resolve();
            }),
        ),
      );
      const nodeRectInit = node.getBoundingClientRect();
      const pageWidth = CAPTURE_PAGE_WIDTH;
      const initialHeight = nodeRectInit.height;
      const isMultiPage = initialHeight > maxPageHeight;
      const contentEdgePage = maxPageHeight - PAGE_BREAK_GUTTER;
      const contentMidPage = maxPageHeight - 2 * PAGE_BREAK_GUTTER;
      let numPagesPlanned = 1;
      if (isMultiPage) {
        const overflow = initialHeight - contentEdgePage;
        const middlePagesNeeded = Math.max(0, Math.ceil((overflow - contentEdgePage) / contentMidPage));
        numPagesPlanned = 2 + middlePagesNeeded;
        const targetTotalHeight =
          2 * contentEdgePage + (numPagesPlanned - 2) * contentMidPage;
        const need = targetTotalHeight - initialHeight;
        if (need > 0) {
          const s = document.createElement("div");
          s.style.height = `${need}px`;
          s.style.flexShrink = "0";
          s.style.background = "#ffffff";
          node.insertBefore(s, footerEl);
          spacerEls.push(s);
        }
      }
      const [{ toJpeg }, { default: jsPDF }] = await Promise.all([
        import("html-to-image"),
        import("jspdf"),
      ]);
      const dataUrl = await toJpeg(node, {
        cacheBust: true,
        pixelRatio: 1.5,
        quality: 0.93,
        backgroundColor: "#ffffff",
        filter: (n) => !(n instanceof HTMLElement) || n.dataset.skipScreenshot !== "true",
      });
      const totalHeight = node.getBoundingClientRect().height;
      const pageHeight = isMultiPage ? maxPageHeight : totalHeight;
      const numPages = numPagesPlanned;
      const pdf = new jsPDF({
        orientation: pageHeight >= pageWidth ? "portrait" : "landscape",
        unit: "px",
        format: [pageWidth, pageHeight],
        hotfixes: ["px_scaling"],
        compress: true,
      });
      for (let i = 0; i < numPages; i++) {
        if (i > 0) pdf.addPage([pageWidth, pageHeight], "portrait");
        if (isMultiPage) {
          const isFirstPage = i === 0;
          const isLastPage = i === numPages - 1;
          const imageStartForPage =
            isFirstPage ? 0 : contentEdgePage + (i - 1) * contentMidPage;
          const pageStartY = isFirstPage ? 0 : PAGE_BREAK_GUTTER;
          pdf.addImage(
            dataUrl,
            "JPEG",
            0,
            pageStartY - imageStartForPage,
            pageWidth,
            totalHeight,
            undefined,
            "FAST",
          );
          pdf.setFillColor(255, 255, 255);
          if (!isFirstPage) {
            pdf.rect(0, 0, pageWidth, PAGE_BREAK_GUTTER, "F");
          }
          if (!isLastPage) {
            pdf.rect(0, pageHeight - PAGE_BREAK_GUTTER, pageWidth, PAGE_BREAK_GUTTER, "F");
          }
        } else {
          pdf.addImage(dataUrl, "JPEG", 0, 0, pageWidth, totalHeight, undefined, "FAST");
        }
      }
      if (toolNameLink && pageUrl) {
        pdf.setPage(1);
        const nodeRect = node.getBoundingClientRect();
        const linkRect = toolNameLink.getBoundingClientRect();
        const linkX = linkRect.left - nodeRect.left;
        const linkY = linkRect.top - nodeRect.top;
        pdf.link(linkX, linkY, linkRect.width, linkRect.height, { url: pageUrl });
      }
      pdf.save(`${sanitize(filename)}.pdf`);
      trackToolEvent(toolId, "cta_clicked", { target });
    } catch {
    } finally {
      node.style.padding = prevPadding;
      node.style.borderRadius = prevBorderRadius;
      node.style.overflow = prevOverflow;
      node.style.marginLeft = prevMarginLeft;
      node.style.marginRight = prevMarginRight;
      node.style.position = prevPosition;
      node.style.top = prevTop;
      node.style.left = prevLeft;
      node.style.width = prevWidth;
      node.style.maxWidth = prevMaxWidth;
      node.style.boxSizing = prevBoxSizing;
      skipEls.forEach((el, i) => {
        el.style.display = prevSkipDisplays[i];
      });
      for (const s of spacerEls) {
        if (s.parentNode === node) node.removeChild(s);
      }
      if (headerEl.parentNode === node) {
        node.removeChild(headerEl);
      }
      if (footerEl.parentNode === node) {
        node.removeChild(footerEl);
      }
      if (overlay.parentNode) {
        overlay.parentNode.removeChild(overlay);
      }
    }
  }, [toolId, filename, heading, subtitle, toolName, target, maxPageHeight]);

  return { resultRef, handleDownload };
}
